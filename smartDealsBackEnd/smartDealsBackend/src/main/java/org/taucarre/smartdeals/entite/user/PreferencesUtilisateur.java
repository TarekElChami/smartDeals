package org.taucarre.smartdeals.entite.user;

public class PreferencesUtilisateur {
	
	private Integer nombreDealsAffiche;
	private String localisation;
	
	
	public Integer getNombreDealsAffiche() {
		return nombreDealsAffiche;
	}
	public void setNombreDealsAffiche(Integer nombreDealsAffiche) {
		this.nombreDealsAffiche = nombreDealsAffiche;
	}
	public String getLocalisation() {
		return localisation;
	}
	public void setLocalisation(String localisation) {
		this.localisation = localisation;
	}
	
	
	

}
